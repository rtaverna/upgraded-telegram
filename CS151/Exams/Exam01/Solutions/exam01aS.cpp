int main()
{
	double x, f, g, h;

	std::cout << "Enter a real number: ";
	std::cin >> x;

	f = x * x - 4 * x + 4;
	g = 2 * f * f - 5 * f;
	h = 4 * g * g * g - 7 * g;

	std::cout << "h(g(f(x))) = " << h << "\n";
	return 0;
}

