int main()
{
	double x, f, g, h;

	std::cout << "Enter a real number: ";
	std::cin >> x;

	f = 2 * x * x * x - 6 * x * x;
	g = 4 * f * f - 12 * f + 9;
	h = 5 * g * g + 2 * g - 7;

	std::cout << "h(g(f(x))) = " << h << "\n";
	return 0;
}

